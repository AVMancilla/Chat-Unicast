    private void botonConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConectarActionPerformed
        // TODO add your handling code here:
        comunicacionCliente=new ComunicacionCliente("JOHNGONSALES",campoDirServer.getText(),Integer.parseInt(campoPuertoServ.getText()));
        int resultado=comunicacionCliente.iniciarComunicacion();
        hebraRecibirMensajes=new HebraRecibirMensajes(comunicacionCliente,this);
        hebraRecibirMensajes.start();
        if(resultado==ComunicacionCliente.estadoComunicacionExitosa){
            labelEstado.setText("Conexion Correcta");
        }
        else{
            labelEstado.setText("Error de Conexion");
        }
        
    }//GEN-LAST:event_botonConectarActionPerformed

