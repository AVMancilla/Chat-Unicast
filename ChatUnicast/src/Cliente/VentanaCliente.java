    /**
     * Envia el mensaje al servidor.
     */
    private void enviarMensaje() {
        if(comunicacionCliente!=null && !campoMensaje.getText().equals("")){
            comunicacionCliente.enviarMensajes(campoMensaje.getText());
            campoMensaje.setText("");
        }
       }
